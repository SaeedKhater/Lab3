package tr.edu.maltepe.OOP;

public class Person {
    private int ID;
    private String Type;

    public Person() {
        this.Type=Type;
        this.ID=ID;
    }
    public String getType() {
        return Type;}

    public int getID() {
        return ID;
    }


    public String printString() {
        return "Type=" + getType() + " ID:" + getID();


    }}